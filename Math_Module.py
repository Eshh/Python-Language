import math
import math as m

x = 10

print(dir(math))

print(int(math.sqrt(x)))

print(math.floor(20.9))  # always the non decimal part as it is

print(math.ceil(20.1))   # non decimal part is incremented disregarding the value of decimal

print(math.ceil(20))

print(math.sin(1/2))

print(math.log(2))

print(math.pow(10, 2))

print(math.pi)  # constants in math eg:pi

print(math.e)  # epsilon

# we can use ALIAS to reduce the burden of typing module name again and again,we can import math as m
print(m.e)
